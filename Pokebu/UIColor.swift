import Foundation
import UIKit

extension UIColor {
    class func themeColorGreen() -> UIColor {
        // #4EB8B2
        return UIColor(red: 0.306, green: 0.722, blue: 0.698, alpha: 1.0)
    }
    
    class func themeColorLightGreen() -> UIColor {
        // #50BCB6
        return UIColor(red: 0.314, green: 0.737, blue: 0.714, alpha: 1.0)
    }
    
    class func themeColorRed() -> UIColor {
        // #ED4055
        return UIColor(red: 0.929, green: 0.251, blue: 0.333, alpha: 1.0)
    }
}